package com.icha.javafundamental.percabangan;

public class Switch {
    public static void main (String[] args ){
        int input = 6;
        switch (input){
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            case 6:
                System.out.println("6");
                break;
            default:
                break;
        }
    }
}
